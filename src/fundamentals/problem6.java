public class problem6 {
	public static void main(String[] args)
	{
		int len=args.length;
		if(len==0)
		{
			System.out.println("No values");
		}
		else
		{
			for(int i=0;i<args.length;i++)
			{
				System.out.println(args[i]);
			}
			
		}
			
		
	}

}
